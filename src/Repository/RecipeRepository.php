    public function search(array $search = []): array
    {
        $query = $this->createQueryBuilder('r')
        ->join('r.ingredients', 'i');

        if (QueryService::isNotEmpty($search, 'search')) {/*
            dd($search['search']);*/
            $query
                ->andWhere('i.id IN (:ingredients)')
                ->setParameter('ingredients', $search['search']);
        }

        return $query
            ->getQuery()
            ->getResult();
    }

